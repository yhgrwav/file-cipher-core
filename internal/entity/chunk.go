package entity

import (
	"time"

	"github.com/google/uuid"
)

// Version - способ реализации согласованности между двумя независимыми инстансами БД.
// Пример проблемы:
// выполняется ротация ключа для одного чанка, что требует двух записей в разные хранилища:
// в DB1 должен обновиться uuid -> key (новый ключ)
// в DB2 должен обновиться uuid -> cipher, nonce (новый шифр)
// между этими двумя записями может упасть сеть/процесс, и мы получим рассинхрон:
// в одной базе уже новые данные, а в другой всё ещё старые. Расшифровать такой чанк
// станет невозможно ни старым ключом (данные уже перешифрованы новым), ни новым
// (в DB2 всё ещё лежит старый ciphertext) — данные будут потеряны безвозвратно.
//
// Version решает эту проблему за счёт отказа от перезаписи данных на месте (UPDATE в базе).
// Вместо этого каждое обновление добавляет новую строку с version+1, а предыдущая
// версия остаётся нетронутой в обеих базах. Актуальной считается максимальная version,
// для которой существует пара (запись в DB1 И запись в DB2 одновременно). Если версия
// присутствует только в одной из баз - значит запись прервалась на середине, и мы явно
// знаем, какой именно uuid нужно решифровать повторно: достаточно сравнить
// обеих базах и найти несовпадения. Это и даёт возобновляемость после сбоя без риска потери данных.
//
// Альтернативный подход - интеграция брокера сообщений с гарантией доставки
// (например, через outbox pattern: запись данных и "намерения отправить событие"
// в одной транзакции одной базы, с последующей асинхронной доставкой в брокер).
// Такой подход лучше подходит для продакшн-нагрузки с высокой частотой записи,
// но избыточен по сложности для объёма и требований этого тестового задания.

type ChunkData struct {
	UUID       uuid.UUID
	Ciphertext []byte
	Nonce      []byte
	Version    int
	CreatedAt  time.Time
}

type ChunkKey struct {
	UUID      uuid.UUID
	Key       []byte
	Version   int
	CreatedAt time.Time
}
